# @lc code=start
"""
# Definition for a Node.
class Node:
    def __init__(self, val=None, children=None):
        self.val = val
        self.children = children
"""

class Solution:
    def postorder(self, root: 'Node') -> List[int]:
        res=[]
        def recursion (root):
            res.append(root.val)
            for child in root.children:
                recursion(child)
        recursion(root)
        return res
# @lc code=end

